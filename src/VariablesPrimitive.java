public class VariablesPrimitive {
    public static void main(String[] args) {
        int myInt = 10;
        short myShort = 5569;
        long myLong = 6546548;

        double myDouble = 54.45;
        float myFloat = 687.4f;

        char myChar = 's';

        boolean myBoolean = true;

        byte myByte = 100;

        System.out.println(myInt);
    }
}
